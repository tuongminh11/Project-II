void sendData(uint8_t data[5]) {
	tx_data[0] = HEADER_HIGH;
	tx_data[1] = HEADER_LOW;

	for (uint8_t i = 0; i < 5; i++) {
		tx_data[i + 2] = data[i];
	}
	uint8_t checksum = 0;
	for (uint8_t i = 0; i < 7; i++) {
		checksum += tx_data[i];
	}
	tx_data[7] = checksum;
	HAL_UART_Transmit(&huart1, tx_data, sizeof(tx_data), 100);
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART1) {
		if (count == 0) {
			if (uartData == HEADER_HIGH) {
				buffer[count] = uartData;
				count++;
			} else {
				count = 0;
			}
		}
		// check header
		else if (count == 1) {
			if (uartData == HEADER_LOW) {
				buffer[count] = uartData;
				count++;
			} else {
				count = 0;
			}
		}
		// save data
		else if (count > 1 && count < 8) {
			buffer[count] = uartData;
			count++;
					if (count == 8) {
			count = 0;
			uint8_t checksum = 0;
			for (uint8_t i = 0; i < 7; i++) {
				checksum += buffer[i];
			}
			if (buffer[7] != checksum) {
				uint8_t tx_data_error[] = {'e', 'r', 'r', 'o', 'r'};
				HAL_UART_Transmit(&huart1, tx_data_error, 8, 100);
			}
			process(buffer);
		}
		}
		// check checksum & process

		HAL_UART_Receive_IT(&huart1, &uartData, 1);
	}
}
